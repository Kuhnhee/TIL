from pprint import pprint
import math

#홈 방범 서비스

T = int(input())
for t in range(1, T+1):
    N, M = map(int, input().split())
    field = []
    population = 0
    for n in range(N):
        row = list(map(int, input().split()))
        field.append(row)
        population += row.count(1)

    # pprint(field)
    # K 범위 : 1 ~ 전체 집이 포함될 때까지
    # 방범 영역 : [0,0] 부터 [N-1,N-1]까지

    '''
    K가 n일때 최소 필요한 마을의 수?

    유지비용 C = n**2 + (n-1)**2
    최소 고객수 minP = math.ceil(C/M)

    전체 마을인구(P)로 유지 가능한 최대 K
    n**2 + (n-1)**2 <= P*M 를 만족하는 최대 n
    '''

    cmax = 1 #K=1이면 최소한 한 가정 가능.

    k = 2
    while True:
        cost = k**2 + (k-1)**2
        if cost > population*M:
            break

        for r in range(N):
            for c in range(N):
                #check this window
                local_population = 0
                for i in range(-k+1, k):
                    for j in range(-(k-abs(i))+1, k-abs(i)):
                        if (not 0<=r+j<N) or (not 0<=c+i<N):
                            continue
                        if field[r+j][c+i] == 1:
                            local_population += 1
                if cost > local_population*M:
                    continue
                else:
                    if local_population > cmax:
                        cmax = local_population

        # make window larger
        k += 1

    print('#{} {}'.format(t, cmax))

